/*-------------------------------------------------
函数名:FUN_String_StrLink
功   能:字符串连接 函数
参   数:
       u8 *sp：目标字符串首地址(以\0结尾)
       u16 sl：目标字符串空间字节数
       u8 *dp：待连接字符串首地址(以\0结尾)
返回值:
       连接后字符串的有效字节数(不包含\0)
注   意:
       sp和dp必需是字符串，且是以\0结尾的，且小于65535个字符
示   例:
作   者:YJX
版   本:V1.0
时   间:2023-03-08
-------------------------------------------------*/
u16 FUN_String_StrLink(u8 *sp,u16 sl,u8 *dp)
{
     u16 u16intex;
     u16 u16yei;//原结束符索引号
     u16 u16wi;//起始添加索引号
     
     if(sl == 0)
     {
          return 0;
     }
     //寻找字符串结束符
     u16yei = sl;
     for(u16intex = 0;u16intex < sl;u16intex++)
     {
          if(sp[u16intex] == '\0')
          {
               u16yei = u16intex;
               break;
          }
     }
     //空间不够存放更多的数据
     if((u16yei == sl) || (1 == (sl - u16yei)))
     {
          return u16yei;
     }
     //开始添加
     u16wi = u16yei;//设置添加索引号
     for(u16intex = 0;u16intex < (sl - u16yei);u16intex++)
     {          
          sp[u16wi++] = dp[u16intex];   
          if(dp[u16intex] == '\0')
          {
               return (u16wi - 1);
          }       
     }
     //添加到空间满都没有找到dp的结束符，则恢复原来(不添加)
     sp[u16yei] = '\0';
     return u16yei;
}

